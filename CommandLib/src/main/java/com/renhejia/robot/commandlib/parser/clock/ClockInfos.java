package com.renhejia.robot.commandlib.parser.clock;

public class ClockInfos {

    private int clock_id;
    private String action;
    private ClockInfo clock_info;

    public int getClock_id() {
        return clock_id;
    }

    public void setClock_id(int clock_id) {
        this.clock_id = clock_id;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public ClockInfo getClock_info() {
        return clock_info;
    }

    public void setClock_info(ClockInfo clock_info) {
        this.clock_info = clock_info;
    }


}
